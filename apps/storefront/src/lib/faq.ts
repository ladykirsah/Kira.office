export interface FaqItem {
  q: string;
  a: string;
}

/**
 * Answer markup: answers are plain strings; "\n" separates lines, lines starting with "N. " form
 * numbered steps, {b}…{/b} marks a blue emphasis span, and {l:/path}…{/l} marks an internal link
 * (rendered red + underlined). OWNER'S COLOR RULE: red = clickable only, blue = non-clickable
 * emphasis — so {r}…{/r} (red span) exists in the parser but must NOT be used in content, it would
 * read as a broken link. The page renders the parsed blocks; JSON-LD uses the stripped plain text.
 */
export type FaqAnswerPart = { text: string; color?: "red" | "blue"; href?: string };
export type FaqAnswerBlock =
  { type: "p"; parts: FaqAnswerPart[] } | { type: "steps"; items: FaqAnswerPart[][] };

const STEP_PREFIX = /^\d+\.\s+/;
const ANSWER_TOKEN = /\{(r|b)\}(.*?)\{\/\1\}|\{l:([^}]+)\}(.*?)\{\/l\}/g;

function parseParts(line: string): FaqAnswerPart[] {
  const parts: FaqAnswerPart[] = [];
  let last = 0;
  for (const m of line.matchAll(ANSWER_TOKEN)) {
    if (m.index > last) parts.push({ text: line.slice(last, m.index) });
    if (m[1]) parts.push({ text: m[2], color: m[1] === "r" ? "red" : "blue" });
    else parts.push({ text: m[4], href: m[3] });
    last = m.index + m[0].length;
  }
  if (last < line.length) parts.push({ text: line.slice(last) });
  return parts.length > 0 ? parts : [{ text: "" }];
}

export function parseFaqAnswer(a: string): FaqAnswerBlock[] {
  const blocks: FaqAnswerBlock[] = [];
  for (const line of a.split("\n")) {
    if (STEP_PREFIX.test(line)) {
      const item = parseParts(line.replace(STEP_PREFIX, ""));
      const prev = blocks[blocks.length - 1];
      if (prev?.type === "steps") prev.items.push(item);
      else blocks.push({ type: "steps", items: [item] });
    } else {
      blocks.push({ type: "p", parts: parseParts(line) });
    }
  }
  return blocks;
}

/** Anchor id for a question card, so answers can deep-link each other with {l:#...} tokens. */
export function faqAnchorId(q: string): string {
  return q.replace(/\s+/g, "-");
}

export function faqAnswerPlainText(a: string): string {
  return a
    .replace(ANSWER_TOKEN, (_m, color, colorText, _href, linkText) =>
      color ? colorText : linkText,
    )
    .split("\n")
    .join(" ");
}
