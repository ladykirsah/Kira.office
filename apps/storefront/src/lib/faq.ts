/** Flatten every section's Q&A into one schema.org FAQPage (the schema has no notion of sections). */
export function faqPageJsonLd(sections: FaqSection[]): Record<string, unknown> {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: sections.flatMap((s) =>
      s.items.map((it) => ({
        "@type": "Question",
        name: it.q,
        acceptedAnswer: { "@type": "Answer", text: it.a },
      })),
    ),
  };
}
